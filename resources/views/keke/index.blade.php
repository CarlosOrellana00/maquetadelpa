          function isValidPassword(PASS){



            $('.mensaje-pass-equal').hide();
            $('.mensaje-validacion-password-update').hide();
            $('.mensaje-validacion-password-update-confirm').hide();



            var status = true;


        //Validacion 1 [Alfanumerica]
        var regex = /^.*[a-zA-Z0-9].*$/;
        if(regex.test(PASS)){
          $('.validation-1').show();
          $('.validar-1').hide();
        }
        else{
          $('.validation-1').hide();
          $('.validar-1').show();
          status = false;
        }



        //Validacion 2 [Min 8 caracteres]
        if(PASS.length>=8){
          $('.validation-2').show();
          $('.validar-2').hide();
        }
        else{
          $('.validation-2').hide();
          $('.validar-2').show();
          status = false;
        }


        //Validacion 3 [Min 3 letras]
        var regex = /^(.*[a-zA-Z]){3}.*$/;
        if(regex.test(PASS)){
          $('.validation-3').show();
          $('.validar-3').hide();
        }
        else{
          $('.validation-3').hide();
          $('.validar-3').show();
          status = false;
        }


        //Validacion 4 [1 Mayuscula]
        var regex = /^.*[A-Z]{1,}.*$/;
        if(regex.test(PASS)){
          $('.validation-4').show();
          $('.validar-4').hide();
        }
        else{
          $('.validation-4').hide();
          $('.validar-4').show();
          status = false;
        }



        //Validacion 5 [Especial]
        var regex = /^.*[!@#$%^&+_*\-=.]{1,}.*$/;
        if(regex.test(PASS)){
          $('.validation-5').show();
          $('.validar-5').hide();
        }
        else{
          $('.validation-5').hide();
          $('.validar-5').show();
          status = false;
        }









        return status;



      }

